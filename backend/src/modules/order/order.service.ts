/**
 * Admin: Fetch all active orders for the Logistics Command Center.
 * "Active" means any order not yet SETTLED or CANCELLED.
 */
export const getActiveOrdersForAdmin = async (): Promise<IOrder[]> => {
  return Order.find({
    status: {
      $nin: [OrderStatus.SETTLED, OrderStatus.CANCELLED],
    },
  })
    .sort({ createdAt: -1 })
    .lean()
    .exec() as Promise<IOrder[]>;
};

/**
 * Admin: Manually re-triggers dispatch for an AWAITING_DRIVER order.
 * Resets the dispatch attempt list and restarts the cascade from
 * the initial radius for the order's vehicle class.
 */
export const rerouteOrder = async (orderId: string): Promise<IOrder> => {
  const order = await Order.findById(orderId).exec();

  if (!order) {
    throw new AppError('Order not found.', 404, 'ORDER_NOT_FOUND');
  }

  if (order.status !== OrderStatus.AWAITING_DRIVER) {
    throw new AppError(
      `Only AWAITING_DRIVER orders can be re-routed. Current status: ${order.status}`,
      400,
      'INVALID_STATUS_FOR_REROUTE'
    );
  }

  // Clear all previous dispatch attempts so excluded driver list resets.
  // Admin manual re-route gives ALL drivers a fresh chance — the previous
  // cascade may have been exhausted due to a temporary unavailability spike.
  const updatedOrder = await Order.findByIdAndUpdate(
    orderId,
    {
      status: OrderStatus.AWAITING_DISPATCH,
      dispatchAttempts: [],
    },
    { new: true }
  ).exec();

  // Re-enable any driver that was left in isAvailable: false
  // from a timed-out offer that was never cleaned up
  await User.updateMany(
    { role: UserRole.TRANSPORTER, 'transporterProfile.isAvailable': false },
    { 'transporterProfile.isAvailable': true }
  );

  // Restart cascade from scratch
  await initiateDispatch(updatedOrder!);

  logger.info(
    `Order ${orderId} manually re-routed by admin. Dispatch cascade restarted.`
  );

  return updatedOrder!;
};