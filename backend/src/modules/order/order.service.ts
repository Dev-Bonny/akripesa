// ─── Google Distance Matrix ───────────────────────────────────────────────────

interface DistanceMatrixResult {
  distanceKm: number;
  durationMinutes: number;
}

const getDistanceFromGoogle = async (
  origin: [number, number],
  destination: [number, number]
): Promise<DistanceMatrixResult> => {
  const response = await axios.get(
    'https://maps.googleapis.com/maps/api/distancematrix/json',
    {
      params: {
        origins: `${origin[1]},${origin[0]}`,
        destinations: `${destination[1]},${destination[0]}`,
        key: env.GOOGLE_MAPS_API_KEY,
        units: 'metric',
      },
      timeout: 10_000,
    }
  );

  const element = response.data?.rows?.[0]?.elements?.[0];

  if (!element || element.status !== 'OK') {
    throw new AppError(
      'Could not calculate distance between locations.',
      502,
      'DISTANCE_MATRIX_FAILED'
    );
  }

  return {
    distanceKm: element.distance.value / 1000,
    durationMinutes: Math.ceil(element.duration.value / 60),
  };
};

// ─── OTP Dispatch (Tri-Party Security Model) ──────────────────────────────────

/**
 * SECURITY INVARIANT — enforced throughout this module:
 *
 * OTP 1 (Loading/Pickup):
 * Generated by system → sent ONLY to Farmer (B2B) or Vendor (B2C).
 * The driver NEVER receives OTP 1. They input it after the farmer dictates it.
 *
 * OTP 2 (Delivery):
 * Generated by system → sent ONLY to Buyer/Consumer.
 * The driver NEVER receives OTP 2. They input it after the buyer dictates it.
 *
 * This physical handshake proves presence at both locations.
 * Any code path that returns an OTP to a TRANSPORTER role is a security violation.
 */

/**
 * Sends OTP 1 to the source party (Farmer or Vendor).
 * Stubbed for Sprint 8 — Africa's Talking SMS client will replace the stub.
 */
const sendLoadingOtpToSourceParty = async (
  recipientPhone: string,
  recipientName: string,
  rawOtp: string,
  orderId: string
): Promise<void> => {
  const shortOrderRef = orderId.slice(-6).toUpperCase();
  const message =
    `Akripesa: Your pickup verification code for order #${shortOrderRef} ` +
    `is ${rawOtp}. Dictate this to the driver when they arrive. Do not share via SMS/WhatsApp.`;

  if (env.NODE_ENV !== 'production') {
    // DEVELOPMENT ONLY — never log OTPs in production
    logger.debug(
      `[OTP STUB - SOURCE] Recipient: ${recipientPhone} (${recipientName}) | ` +
      `Order: ${orderId} | OTP: ${rawOtp}`
    );
    return;
  }

  // Sprint 8: await atSmsClient.send({ to: recipientPhone, message });
  logger.info(
    `Loading OTP dispatched to source party ${recipientPhone} for order ${orderId}.`
  );
};

